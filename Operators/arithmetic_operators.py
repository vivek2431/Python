# Arithmetic Operators

# Addition
a = 5
b = 3
sum_result = a + b
print("Sum:", sum_result)  # Output: 8

# Subtraction
difference = a - b
print("Difference:", difference)  # Output: 2

# Multiplication
product = a * b
print("Product:", product)  # Output: 15

# Division
quotient = a / b
print("Quotient:", quotient)  # Output: 1.6666666666666667

# Modulus (Remainder)
remainder = a % b
print("Remainder:", remainder)  # Output: 2

# Exponentiation
power = a ** b
print("Power:", power)  # Output: 125

# Floor Division (Integer Division)
floor_division = a // b
print("Floor Division:", floor_division)  # Output: 1
